    public static double getMultiplier(String attackType, String defenseType) {
        int attackIndex = getIndex(attackType);
        int defenseIndex = getIndex(defenseType);
        return CHART[attackIndex][defenseIndex];
    }

    private static int getIndex(String type) {
        for (int i = 0; i < TYPE_ORDER.length; i++) {
            if (TYPE_ORDER[i].equalsIgnoreCase(type)) {
                return i;
            }
        }
        throw new IllegalArgumentException("Invalid type: " + type);
    }
